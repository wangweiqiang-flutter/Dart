/**
 * Dart的命名规则：
 * 1、变量名称必须由数字、字母、下划线和美元符($)组成
 * 2、命名开头不能是数字
 * 3、命名不能是保留字和关键字
 * 4、变量的名字是区分大小写的如：age和Age是不同的变量。在实际的运用中，建议不要用一个单词大小写区分两个变量。
 * 5、命名一定要见名思意：变量名称建议用名词，方法名称建议用动词
 */
void main(List<String> args) {
  String name = '孙悟空';
  int age = 500;

  print('姓名：$name');
  print('年龄：$age');

  printAge() {
    print('姓名：$name');
  }
}

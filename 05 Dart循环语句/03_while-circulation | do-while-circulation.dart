/**
 * while循环 和 do while循环
 */
void main(List<String> args) {
  /* 
    while(表达式/循环条件) {			
			语句/循环体
		}	
    		
		do{
			语句/循环体
		} while(表达式/循环条件);

		注意： 1、最后的分号不要忘记
			    2、循环条件中使用的变量需要经过初始化
		      3、循环体中，应有结束循环的条件，否则会造成死循环。
  */

  int i = 1;
  while (i < 10) {
    print('i: $i');
    i++;
  }

  print('————————————————————————————————————');

  // 求1+2+3+4 ...+100的和
  int n = 1;
  int sum = 0;

  do {
    sum += n;
    n++;
  } while (n <= 100);
  print('1~100的和：$sum');
}
